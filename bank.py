# 1) Класс BankAccount — описывает банковский счёт клиента.
# Атрибуты: owner — имя владельца счёта. 
# balance — текущий баланс (по умолчанию 0).
# Методы:deposit(amount) — пополняет счёт на указанную сумму. Если сумма отрицательная, вывести сообщение об ошибке.
# withdraw(amount) — снимает указанную сумму, если средств достаточно. Иначе вывести сообщение об ошибке.
# info() — выводит информацию о владельце и текущем балансе.
